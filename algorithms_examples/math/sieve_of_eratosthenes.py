# Решето Эратосфена - это алгоритм, который помогает найти все простые числа в отрезке от 1 до nfrom typing import List

def sieve(n: int) -> list[int]:  
    if n < 2:                    # Если n меньше 2 — простых чисел нет
        return []                # Возвращаем пустой список

    is_prime = [True] * (n + 1)  # Создаём список из n+1 элементов, все True (считаем все числа простыми)
    is_prime[0] = is_prime[1] = False  # 0 и 1 не являются простыми, помечаем их как False

    p = 2                        # Начинаем с первого простого числа — 2
    while p * p <= n:            # Проверяем только числа до sqrt(n)
        if is_prime[p]:          # Если текущее число всё ещё простое
            # Зачёркиваем все кратные числа, начиная с p*p, шагом p
            for multiple in range(p * p, n + 1, p):
                is_prime[multiple] = False  # Помечаем кратное число как составное (непростое)
        p += 1                   # Переходим к следующему числу

    # Собираем все индексы, где значение True — это и есть простые числа
    return [i for i, prime in enumerate(is_prime) if prime]


print(sieve(20))
